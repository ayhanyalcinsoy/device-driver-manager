# 71 = not supported

def checkNvidiaID(pciId):
    foundGpu = []
    pciId = pciId.upper()
    gpus = getNvidia_304()
    for gpu in gpus:
        if pciId in gpu[1]:
            foundGpu.append(304)
            foundGpu.append('Nvidia ' + gpu[0])
            foundGpu.append(pciId)
            break
    if not foundGpu:
        gpus = getNvidia_173()
        for gpu in gpus:
            if pciId in gpu[1]:
                foundGpu.append(173)
                foundGpu.append('Nvidia ' + gpu[0])
                foundGpu.append(pciId)
                break
    if not foundGpu:
        gpus = getNvidia_96()
        for gpu in gpus:
            if pciId in gpu[1]:
                foundGpu.append(96)
                foundGpu.append('Nvidia ' + gpu[0])
                foundGpu.append(pciId)
                break
    if not foundGpu:
        gpus = getNvidia_71()
        for gpu in gpus:
            if pciId in gpu[1]:
                foundGpu.append(71)
                foundGpu.append('Nvidia ' + gpu[0])
                foundGpu.append(pciId)
                break
    return foundGpu

